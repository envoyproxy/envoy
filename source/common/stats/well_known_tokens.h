// Statically-assigned, lock-free symbol IDs for common stat tokens. Symbols
// 1..WellKnownTokensNum-1 are reserved for this table; dynamic tokens start at
// FirstValidSymbol.
//
// Properties relied on by the rest of SymbolTable:
//   * Index 0 is unused (matches LiteralStringIndicator).
//   * Indices 1..WellKnownTokensNum-1 are the symbol IDs; the slot at that index
//     is the string for that symbol.
//   * These entries are never inserted into encode_map_/decode_map_ and have
//     no ref count.
//
// The array itself costs almost nothing (it only holds string_view pointers into
// read-only binary data), so growing this list is cheap. The one cost is that a
// symbol > 127 takes 2 varint bytes instead of 1 in every StatName that
// references it. Therefore the *hottest* tokens (scope prefixes, per-connection
// and per-request counters, common response codes) are kept in the first 127
// slots, and the lower-frequency long tail is appended afterwards. Symbol IDs are
// derived from the array index and never persisted across processes, so this list
// may be freely reordered or extended between builds.
//
// Sourced from the canonical COUNTER/GAUGE/HISTOGRAM/TEXT_READOUT macros across
// Envoy's core subsystems, the per-response-code stats generated by CodeStatsImpl,
