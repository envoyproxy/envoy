class QuicSniInputTest : public testing::Test {
protected:
  // Helper: create UdpMatchingData with a given buffer.
  ::Envoy::Matcher::DataInputGetResult
  getResult(const std::vector<uint8_t>& raw_bytes) {
    Buffer::OwnedImpl buffer;
    if (!raw_bytes.empty()) {
      buffer.add(raw_bytes.data(), raw_bytes.size());
    }
    auto local = Network::Utility::parseInternetAddressAndPortNoThrow("142.250.80.46:443");
    auto remote = Network::Utility::parseInternetAddressAndPortNoThrow("172.31.5.20:52341");
    Network::Matching::UdpMatchingDataImpl data(*local, *remote, buffer);
    return input_.get(data);
  }

  QuicSniInput input_;
};

// An empty buffer returns monostate.
TEST_F(QuicSniInputTest, EmptyBuffer) {
  auto result = getResult({});
  EXPECT_EQ(result.data_availability_,
            ::Envoy::Matcher::DataInputGetResult::DataAvailability::AllDataAvailable);
  EXPECT_TRUE(absl::holds_alternative<absl::monostate>(result.data_));
}

// A DNS query (starts with a transaction ID, not QUIC) returns monostate.
TEST_F(QuicSniInputTest, NonQuicPacket) {
  // Simulated DNS query: first byte has bit7=0 (Short Header style, not QUIC Long Header)
  std::vector<uint8_t> dns_query = {
      0x4A, 0x01, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
      0x06, 0x67, 0x6F, 0x6F, 0x67, 0x6C, 0x65, 0x03, 0x63, 0x6F, 0x6D,
      0x00, 0x00, 0x01, 0x00, 0x01};
  auto result = getResult(dns_query);
  EXPECT_TRUE(absl::holds_alternative<absl::monostate>(result.data_));
}

// A QUIC Short Header packet (bit7=0 or not Initial type) returns monostate.
TEST_F(QuicSniInputTest, QuicShortHeader) {
  // Short Header: first byte bit7 = 0
  std::vector<uint8_t> short_header(100, 0x40); // 0x40 = short header form
  auto result = getResult(short_header);
  EXPECT_TRUE(absl::holds_alternative<absl::monostate>(result.data_));
}

// A truncated packet (too short to be a valid QUIC Initial) returns monostate.
TEST_F(QuicSniInputTest, TruncatedPacket) {
  // Long Header Initial form byte but only 5 bytes total (missing version, DCID, etc.)
  std::vector<uint8_t> truncated = {0xC0, 0x00, 0x00, 0x00, 0x01};
  auto result = getResult(truncated);
  EXPECT_TRUE(absl::holds_alternative<absl::monostate>(result.data_));
}

// A Long Header with an unknown version returns monostate.
TEST_F(QuicSniInputTest, UnknownVersion) {
  // Build a minimal Long Header Initial with a bogus version 0xDEADBEEF
  std::vector<uint8_t> unknown_ver(1200, 0x00);
  unknown_ver[0] = 0xC3;             // Long Header (bit7=1), Initial (bits4-5 vary by version)
  unknown_ver[1] = 0xDE;             // Version byte 1
  unknown_ver[2] = 0xAD;             // Version byte 2
  unknown_ver[3] = 0xBE;             // Version byte 3
  unknown_ver[4] = 0xEF;             // Version byte 4
  unknown_ver[5] = 0x08;             // DCID length
  // Rest is zeros (invalid but sufficient for header parsing to detect unknown version)
  auto result = getResult(unknown_ver);
  EXPECT_TRUE(absl::holds_alternative<absl::monostate>(result.data_));
}

